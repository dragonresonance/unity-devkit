using UnityEngine.Events;
using UnityEngine;


namespace DragonResonance.Events
{
	[CreateAssetMenu(menuName = "Dragon Resonance/Events/Int Event Channel")]
	public class SOIntEventChannel : ScriptableObject
	{
		public UnityAction<int> OnEventRaised;

		public void RaiseEvent(int data) => OnEventRaised?.Invoke(data);
	}
}


